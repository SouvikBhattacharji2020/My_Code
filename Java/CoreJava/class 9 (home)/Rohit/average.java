class average 
{
    public static void main (String args [])
    {
        double s1=4325.0d,s2=4996.5d,s3=8935.5d,s4=9960.75d;
        
        double tot=s1+s2+s3+s4;
        double avg=tot/4.0d;
        
        System.out.println("total sales="+tot);
        System.out.println("average sales ="+avg);
    }
}
  