class Switch_case_pro_1
{
	public static void main(String args[])
	{
		int i=3;
		switch(i)
		{
			case 1:
			System.out.println("You Enter 1");
			break;
			case 2:
			System.out.println("You Enter 2");
			break;
			case 3:
			System.out.println("You Enter 3");
			break;
			case 4:
			System.out.println("You Enter 4");
			break;
			case 5:
			System.out.println("You Enter 5");
			break;
			case 6:
			System.out.println("You Enter 6");
			break;
			case 7:
			System.out.println("You Enter 7");
			break;
			default:
			System.out.println("We don't know.");
			break;
		}
	}
}